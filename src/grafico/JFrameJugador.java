    private void btnAgregarJugadorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarJugadorMouseClicked
        // TODO add your handling code here:
        String Apellido = txtApellido.getText();
        
       /* String Camiseta = txtCamiseta.getText();
        String Dia = txtDia.getText();
        String Mes = txtMes.getText();
        String Anio = txtAnio.getText();*/
        
        int Camisa = Integer.parseInt(txtCamiseta.getText());
        int conviertoDia = Integer.parseInt(txtDia.getText());
        int conviertoMes = Integer.parseInt(txtMes.getText());
        int conviertoAnio = Integer.parseInt(txtAnio.getText());
        
        Seleccion seleccion = new Seleccion();
        
        Fecha fecha = new Fecha();
        fecha.setDia(conviertoDia);
        fecha.setMes(conviertoMes);
        fecha.setAnio(conviertoAnio);
        
        seleccion.InsertarJugador(Apellido, Camisa, fecha);
        System.out.println(seleccion.mostrarJugadores());
        
        
    }//GEN-LAST:event_btnAgregarJugadorMouseClicked

